	processor 16f877
	include<p16f877.inc>
ORG 0
GOTO INICIO
ORG 5
INICIO:
	BSF STATUS,RP0	;Cambio al banco 1
	BCF STATUS,RP1
	CLRF PORTB		;PORTB = 0
	CLRF TRISB		;Configuro puerto B como salida
	BSF TXSTA,BRGH  ;BRGH = 1
	MOVLW D'129'	;W = 129
	MOVWF SPBRG		;SPBRG = W = 129
	BCF TXSTA,SYNC	;SYNC = 0
	BSF TXSTA,TXEN	;TXEN = 1
	BCF STATUS,RP0	;Cambio al banco 0
	BSF RCSTA,SPEN	;SPEN  = 1
	BSF RCSTA,CREN	;CREN = 1
	MOVLW A'H'
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A'O'
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A'L'
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A'A'
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A' '
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A'U'
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A'N'
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A'A'
	MOVWF TXREG
	CALL TRANSMITE
	MOVLW A'M'
	MOVWF TXREG
	CALL TRANSMITE
TRANSMITE:
	BSF STATUS,RP0	;Cambio al banco 1
TRANSMITE1:
	BTFSS TXSTA,TRMT;if (TRMT = 1) {Salta}
	GOTO TRANSMITE1	;else {TRANSMITE}
	BCF STATUS,RP0	;Cambio al banco 0
	RETURN	
END